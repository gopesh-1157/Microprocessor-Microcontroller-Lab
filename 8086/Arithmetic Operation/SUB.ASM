.ORG 2000H

DATA SEGMENT
    NUM1 DW FFFFH
    NUM2 DW AAAAH
    DIFF DW 0000H
    BORROW DB 00H
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE, DS:DATA

START:
    MOV AX, DATA
    MOV DS, AX

    MOV AX, NUM1
    MOV BL, 00H        ; Clear borrow register

    SUB AX, NUM2
    JNC NO_BORROW
    INC BL

NO_BORROW:
    MOV DIFF, AX
    MOV BORROW, BL

    HLT

CODE ENDS
END START
